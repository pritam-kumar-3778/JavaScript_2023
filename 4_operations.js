let a = 20;
let b = 10;

//addition
let sum  = a+b;
console.log(sum);

//substraction
 let sub = a-b;
 console.log(sub);

 //multiplication
 let multiplication = a*b;
 console.log(multiplication);

 //division
 let division = a/b;
 console.log(division);

 //modulo (Reminder operator)
 let reminder = a%b;
 console.log(reminder);

 //exponentiation (Power operator)
 let pow = a**b;
 console.log(pow);